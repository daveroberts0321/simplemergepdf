		<h2>Our Commitment</h2>
		<p>SimpleMergePDF is built on a simple privacy principle: <strong>we collect the minimum data necessary to merge your PDFs, and we delete it as soon as we're done.</strong> We don't track you, we don't profile you, and we don't sell your data.</p>

		<h2>Information We Collect</h2>
		<p>When you use SimpleMergePDF, we collect only what's needed to process your merge:</p>
		<ul>
			<li><strong>Your email address:</strong> Used solely to send you the download link. Not stored after the email is sent.</li>
			<li><strong>Your PDF files:</strong> Used solely for merging into a single PDF.</li>
			<li><strong>Payment information:</strong> Processed entirely by Stripe. We never see or store your card number.</li>
		</ul>

		<h2>What We Don't Collect</h2>
		<ul>
			<li>We do not use analytics or tracking cookies.</li>
			<li>We do not use advertising trackers or pixels.</li>
			<li>We do not collect your IP address for tracking purposes.</li>
			<li>We do not create user profiles.</li>
			<li>We do not share data with data brokers or advertisers.</li>
			<li>We do not use third-party analytics services (no Google Analytics, no Mixpanel, etc.).</li>
		</ul>

		<h2>Data Retention</h2>
		<ul>
			<li><strong>Documents:</strong> Deleted from our servers immediately after you download the merged PDF. If a merge operation fails, documents are retained for retry attempts and deleted within 24 hours.</li>
			<li><strong>Email address:</strong> Used only to send the download link. Not stored in any database or mailing list.</li>
			<li><strong>Merge job records:</strong> Basic transaction records (number of files, status, timestamp) may be retained for refund processing and legal compliance. These records do not contain your documents.</li>
			<li><strong>Payment records:</strong> Managed by Stripe in accordance with their <a href="https://stripe.com/privacy" target="_blank" rel="noopener noreferrer">privacy policy</a>. We retain only the minimum transaction reference needed for refund processing.</li>
		</ul>

		<h2>Third-Party Services</h2>
		<p>We use the following third-party services, each with their own privacy policies:</p>
		<ul>
			<li><strong>Stripe:</strong> Payment processing. <a href="https://stripe.com/privacy" target="_blank" rel="noopener noreferrer">Stripe Privacy Policy</a></li>
			<li><strong>Amazon SES:</strong> Email delivery for download links only.</li>
			<li><strong>Supabase:</strong> Backend infrastructure. Data is stored temporarily as described in the Data Retention section above.</li>
		</ul>

		<h2>Cookies</h2>
		<p>SimpleMergePDF does not use tracking cookies, advertising cookies, or analytics cookies. The only cookies used are essential cookies required for the payment process (set by Stripe's payment form).</p>

		<h2>Children's Privacy</h2>
		<p>SimpleMergePDF is not intended for use by children under the age of 13. We do not knowingly collect information from children under 13.</p>

		<h2>Your Rights</h2>
		<p>Because we collect minimal data and delete it promptly, there is generally no personal data to access, correct, or delete. If you have concerns about any data we may hold, contact us and we will address them promptly.</p>

		<h2>Changes to This Policy</h2>
		<p>We may update this Privacy Policy from time to time. Changes will be posted on this page with an updated revision date.</p>

		<h2>Contact</h2>
		<p>If you have questions about this Privacy Policy, please contact us at <a href="mailto:support@simplemergepdf.com">support@simplemergepdf.com</a>.</p>
