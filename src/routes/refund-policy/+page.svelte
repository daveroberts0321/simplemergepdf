		<h2>Overview</h2>
		<p>SimpleMergePDF charges a flat fee of <strong>$3.99 per merge operation</strong>. Because this is a digital service that is delivered immediately upon payment, we handle refunds on a case-by-case basis as described below.</p>

		<h2>When We Issue Refunds</h2>
		<p>We will issue a <strong>full refund</strong> in the following situations:</p>
		<ul>
			<li><strong>Merge failure:</strong> If your PDF merge fails due to a technical issue on our end, you will receive a full refund automatically.</li>
			<li><strong>Processing error:</strong> If the merged PDF cannot be generated due to a system error after multiple retry attempts, we will refund your payment.</li>
			<li><strong>Duplicate charges:</strong> If you were charged more than once for the same merge operation, we will refund the duplicate charge immediately.</li>
		</ul>

		<h2>When Refunds May Not Apply</h2>
		<p>Refunds are generally <strong>not available</strong> in these cases:</p>
		<ul>
			<li><strong>Wrong files uploaded:</strong> If you uploaded incorrect PDF files and the merge was completed successfully, we cannot reverse the operation.</li>
			<li><strong>Files in wrong order:</strong> If your PDFs were merged in an unintended order but the merge was successful, we cannot issue a refund. You can perform a new merge with the correct order.</li>
			<li><strong>Change of mind:</strong> Once a merge has been successfully completed and the download link sent, we cannot undo the operation.</li>
			<li><strong>Corrupted input files:</strong> If your uploaded PDF files were corrupted or damaged before upload, resulting in merge failure, this is not eligible for refund.</li>
		</ul>

		<h2>How to Request a Refund</h2>
		<p>If you believe you're entitled to a refund, contact us at <a href="mailto:support@simplemergepdf.com">support@simplemergepdf.com</a> with:</p>
		<ul>
			<li>The email address you used for the merge</li>
			<li>The approximate date and time of your transaction</li>
			<li>A brief description of the issue</li>
		</ul>
		<p>We aim to respond to all refund requests within <strong>24 hours</strong> and process approved refunds within <strong>3-5 business days</strong>.</p>

		<h2>Refund Method</h2>
		<p>All refunds are processed through Stripe and will be returned to the original payment method used for the transaction.</p>

		<h2>Contact Us</h2>
		<p>If you have questions about this refund policy, please contact us at <a href="mailto:support@simplemergepdf.com">support@simplemergepdf.com</a>.</p>
